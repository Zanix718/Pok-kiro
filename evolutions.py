def check_evolution_requirements(pokemon_data):
    pokemon_name = pokemon_data["name"].lower()
    current_level = pokemon_data.get("level", pokemon_data.get("Level", 0))
    if pokemon_name in LEVEL_ONLY_EVOLUTIONS:
        evolution_info = LEVEL_ONLY_EVOLUTIONS[pokemon_name]
        required_level = evolution_info["level"]
        if current_level >= required_level:
            return True, evolution_info
    return False, None
def get_evolution_info(pokemon_name):
    pokemon_name_lower = pokemon_name.lower()
    if pokemon_name_lower in LEVEL_ONLY_EVOLUTIONS:
        return LEVEL_ONLY_EVOLUTIONS[pokemon_name_lower]
    return None